package com.inventario_hardware.inventario.model;

import jakarta.persistence.*;
// com.inventario_hardware.inventario.model.Pantallas
@Entity @Table(name="pantallas")
public class Pantallas {
    @Id @GeneratedValue(strategy=GenerationType.IDENTITY) private Long id;
    private String marca; private String modelo;
    @Column(name="numero_de_serie") private String numeroDeSerie;
    private String estado; private String area; private String departamento;
    @Column(name="responsable_y_o_responsiva") private String responsable;
    private String puesto;
    // getters/setters...

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getNumeroDeSerie() {
        return numeroDeSerie;
    }

    public void setNumeroDeSerie(String numeroDeSerie) {
        this.numeroDeSerie = numeroDeSerie;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getResponsable() {
        return responsable;
    }

    public void setResponsable(String responsable) {
        this.responsable = responsable;
    }

    public String getPuesto() {
        return puesto;
    }

    public void setPuesto(String puesto) {
        this.puesto = puesto;
    }
}


